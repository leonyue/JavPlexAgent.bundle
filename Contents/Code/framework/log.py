# noinspection PyPep8Naming
class Log:

    @classmethod
    def Debug(cls, message: str):
        pass

    @classmethod
    def Info(cls, message: str):
        pass

    @classmethod
    def Warn(cls, message: str):
        pass

    @classmethod
    def Error(cls, message: str):
        pass

    @classmethod
    def Critical(cls, message: str):
        pass

    @classmethod
    def Exception(cls, message: str):
        pass
